public class IntPair{
    public int xVal;
    public int yVal;
    public IntPair(){
    }
    public int getXVal(){ return xVal; }
    public int getYVal(){ return yVal; }
    public void setXVal(int xVal){ this.xVal = xVal; }
    public void setYVal(int yVal){ this.yVal = yVal; }
}
